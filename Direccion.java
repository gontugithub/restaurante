package ejercicio1_restaurante;

public class Direccion {
	
	private String localidad;
	private String calle;
	private int numero;
	private int piso;
	private char letra;
	
	public Direccion(String localidad, String calle, int numero, int piso, char letra) {
		this.localidad = localidad;
		this.calle = calle;
		this.numero = numero;
		this.piso = piso;
		this.letra = letra;
	}
	
	
	
	
	

}
